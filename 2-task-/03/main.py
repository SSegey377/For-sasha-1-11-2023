# Здесь пиши программу
tovar = [['каретка', 1200], ['шатун', 1000], ['седло', 300],
         ['педаль', 100], ['седло', 1500], ['рама', 12000],
         ['обод', 2000], ['шатун', 200], ['седло', 2700]]
count = 0
money = int()
f = input("введите название товара: ")
for b in tovar:
     if f in b:
        count += 1
        money += b[1]

print("название товара: ",f)
print("цена товара:",money)
print("кол-во товара: ", count)

