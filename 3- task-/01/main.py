# Здесь пиши программу
al_sp = ["а","А","о","О","е","Е","Ы","ы","Ё","ё", "э","Э","И","и","Я","я","Ю","ю","У","у"]
sp = []
txt =str(input("Текст: "))


for i in txt:
    for j in al_sp:
        if i == j:
            sp.append(i)
print("Список гласных букв: ",sp)
print('Кол-во гласных в тексте: ',len(sp))